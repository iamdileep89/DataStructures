/*
TIME COMPLEXITY

V - No. of Vertices
E - No. of Edges

For Adjacency List
  Adding Vertex - O(1)
  Removing Vertex - O(V+E)
  Adding Edge - O(1)
  Removing Edge - O(E)
  Query - O(V+E)
  Finding Neighbours - O(V)

For Adjacency Matrx
  Adding Vertex - O(V^2)
  Removing Vertex - O(V^2)
  Adding Edge - O(1)
  Removing Edge - O(1)
  Query - O(1)
  Finding Neighbours - O(V)


*/

/*
SPACE COMPLEXITY

For Adjacency List  
  Avg - O(V+E)
  Worst - O(V^2)

For Adjacency Matrx
  Avg - O(V^2)
  Worst - O(V^2)

*/